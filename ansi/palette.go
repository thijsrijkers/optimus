package ansi

import "image/color"

var ansi16 = [16]color.RGBA{
	{0x00, 0x00, 0x00, 0xFF},
	{0xCC, 0x66, 0x66, 0xFF},
	{0xB5, 0xBD, 0x68, 0xFF},
	{0xCC, 0xCC, 0x00, 0xFF},
	{0x00, 0x00, 0xCC, 0xFF},
	{0xCC, 0x00, 0xCC, 0xFF},
	{0x00, 0xCC, 0xCC, 0xFF},
	{0xCC, 0xCC, 0xCC, 0xFF},
	{0x55, 0x55, 0x55, 0xFF},
	{0xD5, 0x4E, 0x53, 0xFF},
	{0xB9, 0xCA, 0x4A, 0xFF},
	{0xFF, 0xFF, 0x55, 0xFF},
	{0x55, 0x55, 0xFF, 0xFF},
	{0xFF, 0x55, 0xFF, 0xFF},
	{0x55, 0xFF, 0xFF, 0xFF},
	{0xFF, 0xFF, 0xFF, 0xFF},
}

var ansi256 [256]color.RGBA

func init() {
	for i := 0; i < 16; i++ {
		ansi256[i] = ansi16[i]
	}
	for r := 0; r < 6; r++ {
		for g := 0; g < 6; g++ {
			for b := 0; b < 6; b++ {
				idx := 16 + 36*r + 6*g + b
				toV := func(v int) uint8 {
					if v == 0 {
						return 0
					}
					return uint8(55 + v*40)
				}
				ansi256[idx] = color.RGBA{toV(r), toV(g), toV(b), 0xFF}
			}
		}
	}
	for i := 0; i < 24; i++ {
		v := uint8(8 + i*10)
		ansi256[232+i] = color.RGBA{v, v, v, 0xFF}
	}
}
